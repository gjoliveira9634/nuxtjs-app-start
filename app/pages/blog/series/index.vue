<script setup lang="ts">
	import { useAsyncData, useHead, useSeoMeta } from "nuxt/app";
	import { computed, watchEffect } from "vue";
	import { usePagination } from "~/composables/usePagination";

	definePageMeta({ layout: "blog" });
	const { t, locale } = useI18n();
	const localePath = useLocalePath() as any;
	const route = useRoute();
	const q = computed(() => (route.query.q as string) || "");

	const title = computed(() => t("post.series"));
	const description = computed(() => t("site.tagline"));
	useSeoMeta({
		title,
		description,
		ogTitle: title,
		ogDescription: description,
		twitterCard: "summary_large_image",
		ogImage: "/og.jpg",
	});
	const i18nHead = useLocaleHead({ addSeoAttributes: true } as any);
	watchEffect(() => {
		const v = (i18nHead as any).value || {};
		useHead({
			htmlAttrs: v.htmlAttrs || {},
			link: v.link || [],
			meta: v.meta || [],
		});
	});
	const { data: series } = await useAsyncData(
		() => `series:${locale.value}`,
		async () => {
			const loc = locale.value.toLowerCase().split("-")[0];
			const all = (await queryCollection("series").all()) as any[];
			return all.filter((s: any) =>
				((s?.path as string) || "").startsWith(`/series/${loc}/`),
			);
		},
		{ watch: [locale] },
	);

	const seriesList = computed(() => {
		const list = (series.value as any[]) || [];
		if (!q.value) return list;
		const s = q.value.toLowerCase();
		return list.filter(
			(item) =>
				(item.title || "").toLowerCase().includes(s)
				|| (item.description || "").toLowerCase().includes(s),
		);
	});

	const {
		page,
		perPage,
		totalItems,
		totalPages,
		pagedItems,
		setPage,
		setPerPage,
	} = usePagination(() => seriesList.value, { defaultPerPage: 9 });

	// Enforce 9 items per page always
	watch(
		() => perPage.value,
		(v) => {
			if (v !== 9) setPerPage(9);
		},
		{ immediate: true },
	);

	const showingFrom = computed(() =>
		totalItems.value ? (page.value - 1) * perPage.value + 1 : 0,
	);
	const showingTo = computed(() =>
		Math.min(totalItems.value, page.value * perPage.value),
	);

	function toPage(p: number) {
		const query: Record<string, any> = { ...route.query, page: p };
		return { query, hash: "#series-list" } as any;
	}

	function seriesSlug(s: any) {
		const p = (s?.path as string) || "";
		if (p) return p.replace(/^\/?series\/[^/]+\//, "");
		return (s?.title || "").toLowerCase().replace(/\s+/g, "-");
	}
</script>

<template>
	<div class="py-8">
		<h1 class="mb-6 text-3xl font-semibold">{{ t("post.series") }}</h1>
		<div
			class="mb-3 flex flex-wrap items-center justify-between gap-3"
			v-if="(pagedItems || []).length && totalPages > 1">
			<div class="text-xs text-gray-600 dark:text-gray-400">
				{{ showingFrom }}–{{ showingTo }} / {{ totalItems }}
			</div>
			<div class="flex items-center gap-3">
				<NuxtPagination
					:page="page"
					:total="totalItems"
					:items-per-page="perPage"
					:sibling-count="1"
					show-edges
					:to="toPage"
					@update:page="setPage" />
			</div>
		</div>

		<div
			v-if="(pagedItems || []).length"
			class="grid gap-4 sm:grid-cols-2 lg:grid-cols-3"
			id="series-list">
			<BlogCardSerie
				v-for="s in pagedItems || []"
				:key="s.title"
				:item="s"
				:to="localePath(`/blog/series/${encodeURIComponent(seriesSlug(s))}`)" />
		</div>
		<div
			v-else
			class="rounded-md border border-gray-200 bg-white p-6 text-center text-sm text-gray-600 dark:border-gray-800 dark:bg-gray-900 dark:text-gray-300">
			{{ t("blog.noResults") }}
		</div>
		<div
			class="mt-4 flex flex-wrap items-center justify-between gap-3"
			v-if="(pagedItems || []).length && totalPages > 1">
			<div class="text-xs text-gray-600 dark:text-gray-400">
				{{ showingFrom }}–{{ showingTo }} / {{ totalItems }}
			</div>
			<div class="flex items-center gap-3">
				<NuxtPagination
					:page="page"
					:total="totalItems"
					:items-per-page="perPage"
					:sibling-count="1"
					show-edges
					:to="toPage"
					@update:page="setPage" />
			</div>
		</div>
	</div>
</template>
